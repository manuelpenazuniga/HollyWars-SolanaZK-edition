const RARITY_PALETTES: Record<
  Medal["rarity"],
  { main: string; hi: string; shade: string }
> = {
  common: { main: "#AEB6C4", hi: "#E3E8F0", shade: "#5C6472" },
  rare: { main: "#4D9FFF", hi: "#A8CFFF", shade: "#1F5FB3" },
  epic: { main: "#A78BFA", hi: "#D6C7FF", shade: "#6D4FC0" },
  legendary: { main: "#F5B32F", hi: "#FFE08A", shade: "#B37B14" },
};

export function PixelMedal({
  sprite,
  rarity,
  size = 64,
  className = "",
}: {
  sprite: SpriteName;
  rarity: Medal["rarity"];
  size?: number;
  className?: string;
}) {
  const grid = SPRITES[sprite];
  const pal = RARITY_PALETTES[rarity];
  const fill = { "#": pal.main, "+": pal.hi, o: pal.shade } as const;

  return (
    <svg
      viewBox="0 0 16 16"
      width={size}
      height={size}
      shapeRendering="crispEdges"
      className={className}
      aria-hidden
    >
      {grid.flatMap((row, y) =>
        [...row].map((ch, x) =>
          ch === "." ? null : (
            <rect
              key={`${x}-${y}`}
              x={x}
              y={y}
              width={1}
              height={1}
              fill={fill[ch as keyof typeof fill]}
            />
          ),
        ),
      )}
    </svg>
  );
}
